    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        DefaultTableModel model = (DefaultTableModel) ListaProductos.getModel();

        producto.setNoSerie(txtSerie.getText());
        producto.setDescripcion(txtDescripcion.getText());
        producto.setCantidad(Integer.parseInt((txtCantidad.getText())));
        producto.setCaducidad(txtCaducidad.getText());
        producto.setProveedor(txtProveedor.getText());

        Object[] row = {producto.getNoSerie(), producto.getDescripcion(), producto.getCantidad()
                , producto.getCaducidad(), producto.getProveedor()};
        model.addRow(row);

        txtSerie.setText("");
        txtDescripcion.setText("");
        txtCantidad.setText("");
        txtCaducidad.setText("");
        txtProveedor.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

